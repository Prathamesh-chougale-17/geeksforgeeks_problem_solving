class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count{0},temp{N};
        int lol = temp%10;
        while(temp){
            if(lol!=0 && N%(lol)==0){
                count++;
            }
            temp/=10;
            lol = temp%10;
        }
        return count;
    }
};

