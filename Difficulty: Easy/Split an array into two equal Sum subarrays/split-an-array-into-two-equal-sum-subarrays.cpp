class Solution {
  public:
    bool canSplit(vector<int>& arr) {
        int left=0,right=arr.size()-1,suml=arr[left],sumr=arr[right];
        while(left<right){
            if((left==right-1) && (sumr==suml)) return true;
            suml<sumr?suml+=arr[++left]:sumr+=arr[--right];
        }
        return false;
    }
};


