class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int maxi=*max_element(arr.begin(),arr.end()),sum_maxi=0;
        for(auto &i:arr){
            if(maxi!=i)sum_maxi=max(sum_maxi,maxi+i);
        }
        return sum_maxi;
    }
};


